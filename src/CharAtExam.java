public class CharAtExam { //문자열의 특정 위치의 문자를 반환하는 메소드
    public static void main(String[] args) {
        String str = "I Love Korea";
        for (int i = 0; i < str.length(); i++) {
            System.out.print(str.charAt(i));
        } //end of for
        System.out.println();
    } //end of main
} //end of CharAtExam